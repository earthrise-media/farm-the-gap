<p>
  Consuming less beef, lamb, and goat offers the largest single positive impact on your personal
  food footprint. While these meats provide only 5% of an average US diet's calories, they account
  for roughly 50% of land use and greenhouse gas emissions from our food system. This
  disproportionate effect stems from the extensive resources these animals require.
</p>

<p>Making mindful changes is surprisingly easy:</p>

<ul>
  <li>
    Plant-Forward Power: Incorporating a couple of vegetarian or vegan meals each week is simple and
    delicious. Explore the world of bean-based chili, lentil soups, or veggie-packed curries.
  </li>
  <li>
    Sustainable Swaps: Opt for chicken, fish, or a sustainably sourced seafood choice for a
    significantly smaller footprint. Even ground meat dishes offer alternatives - think turkey
    burgers or tacos filled with seasoned lentil fillings.
  </li>
  <li>
    Mind Your Portions: Even choosing smaller beef servings helps lessen individual impact. Opt for
    quality over quantity when possible.
  </li>
  <li>
    Flexibility is Key: Unlike stricter dietary overhauls, reducing ruminant meat allows room for
    your favorite dishes while being eco-conscious. Small, consistent shifts go a long way.
  </li>
  <li>
    Innovations on the Horizon: Keep an eye on plant-based and lab-grown meat alternatives in your
    area. As technology advances, these might become additional ways to shrink your food footprint.
  </li>
</ul>

